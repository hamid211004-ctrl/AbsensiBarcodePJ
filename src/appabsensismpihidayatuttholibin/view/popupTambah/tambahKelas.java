    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        //Ambil input dari text field pada GUI 
        String idKelas = tKode.getText();
        String namaKelas = tNama.getText();

        if (cWali.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Wali kelas harus dipilih!");
            return;
        }
        String waliKelas = NIP(cWali.getSelectedItem().toString());

        try {
            //Query SQL untuk menyisipkan data ke tabel kelas
            String sql = "INSERT INTO kelas(id_kelas, nama_kelas, nip) VALUES (?,?,?)";

            //Buat koneksi ke database menggunakan method koneksi() dari class koneksi
            Connection conn = Koneksi.konek();

            //Siapkan query SQL untuk dieksekusi dengan parameter
            PreparedStatement ps = conn.prepareStatement(sql);

            //isi parameter
            ps.setString(1, idKelas);
            ps.setString(2, namaKelas);
            ps.setString(3, waliKelas);

            //Jalankan query untuk menyimpan data ke database
            ps.executeUpdate();

            //Tampilkan pesan bahwa data berhasil disimpan
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");

            pK.load_tabel_kelas();
        } catch (SQLException e) {
            //jika terjadi kesalahan saat menyimpan data, tampilkan pesan
            JOptionPane.showMessageDialog(null, "Data gagal disimpan");
        }
        dispose();
    }//GEN-LAST:event_bSimpanActionPerformed

