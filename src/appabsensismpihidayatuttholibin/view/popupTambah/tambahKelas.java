    void comboWaliKelas() {
        try {
            String sql = "SELECT * FROM guru";
            Connection conn = Koneksi.konek();
            Statement st = conn.createStatement();
            ResultSet resultSet = st.executeQuery(sql);

            while (resultSet.next()) {
                cWali.addItem(resultSet.getString("nama_guru"));
            }
        } catch (SQLException e) {

        }
        cWali.setSelectedItem(null);
    }

    String NIP(String namaGuru) {
        try {
            //query dengan paramenter untuk mencari guru berdasarkan nama 
            String sql = "SELECT * FROM guru WHERE nama_guru = ?";
            //membuka koneksi ke database 
            Connection conn = Koneksi.konek();
            //siapkan preparedStatement 
            PreparedStatement ps = conn.prepareStatement(sql);
            //isi paramenter query dengan nama jurusan 
            ps.setString(1, namaGuru);

            //menjalankan query dan menyimpan hasilnya dalam resultSet 
            ResultSet rs = ps.executeQuery();

            //jika data ditemukan, kembalikan NIP guru 
            while (rs.next()) {
                return rs.getString("nip");
            }
        } catch (SQLException e) {
            //jika error, kembalikan string kosong 
            return "";
        }
        //jika tidak ditemukan, kembalikan string kosong 
        return "";
    }

    private void generateIdKelas() {
        try {
            Connection conn = Koneksi.konek();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT id_kelas FROM Kelas ORDER BY id_kelas DESC LIMIT 1");

            if (rs.next()) {
                //ambil angka setelah ABS
                String id = rs.getString("id_kelas").substring(3);
                int no = Integer.parseInt(id) + 1;

                tKode.setText(String.format("KLS%03d", no));
            } else {
                tKode.setText("KLS001");
            }
            rs.close();
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal generate ID : " + e.getMessage());
        }
    }

