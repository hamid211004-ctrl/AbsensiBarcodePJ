    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
         String idPergantian = tID.getText();

        java.text.SimpleDateFormat formatMulai = new java.text.SimpleDateFormat("yyyy-MM-dd");
        String tanggal = (jTGL.getDate() != null) ? formatMulai.format(jTGL.getDate()) : "";

        String jamKe = tJamke.getText();
        String mapel = tMapel.getText();
        String tugasMateri = tMateri.getText();
        String keterangan = tKeterangan.getText();

        String nipdigantikan = cariNip(cbNamaGuru.getSelectedItem().toString());
        String nipMenggantikan = cariNip(cbGuruPengganti.getSelectedItem().toString());

        String sql = "INSERT INTO pergantian_guru (id_pergantianguru, "
                + "tanggal, "
                + "jam_ke, "
                + "mapel, "
                + "tugas_materi, "
                + "keterangan, "
                + "id_digantikan, "
                + "id_menggantikan) "
                + "VALUES (?,?,?,?,?,?,?,?)";

        try {
            Connection conn = Koneksi.konek();
            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, idPergantian);
            ps.setString(2, tanggal);
            ps.setString(3, jamKe);
            ps.setString(4, mapel);
            ps.setString(5, tugasMateri);
            ps.setString(6, keterangan);
            ps.setString(7, nipdigantikan);
            ps.setString(8, nipMenggantikan);

            ps.execute();

            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            pPerG.load_tabel_pengganti();
            dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan! : " + e.getMessage());
        }
        dispose();
    }//GEN-LAST:event_bSimpanActionPerformed

