    void tampil_combobox() {
        try {
            java.sql.Connection conn = Koneksi.konek();
            java.sql.Statement st = conn.createStatement();

            String sql = "SELECT nama_guru FROM guru";
            java.sql.ResultSet rs = st.executeQuery(sql);

            cbNamaGuru.removeAllItems();
            cbGuruPengganti.removeAllItems();

            while (rs.next()) {
                String nama = rs.getString("nama_guru");
                cbNamaGuru.addItem(nama);
                cbGuruPengganti.addItem(nama);
            }

        } catch (SQLException e) {
            System.out.println("Gagal load combobox: " + e.getMessage());
        }
        cbNamaGuru.setSelectedItem(null);
        cbGuruPengganti.setSelectedItem(null);
    }
    
    private String cariNip(String namaGuru) {
        String idDitemukan = "";
        try {
            java.sql.Connection conn = Koneksi.konek();
            String sql = "SELECT id_guru FROM guru WHERE nama_guru = ?";
            java.sql.PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, namaGuru);
            java.sql.ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                idDitemukan = rs.getString("id_guru");
            }
        } catch (java.sql.SQLException e) {
            System.out.println("Gagal mencari NIP: " + e.getMessage());
        }
        return idDitemukan;
    }
    
    private void generateIdPergantian() {
        try {
            Connection conn = Koneksi.konek();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT id_pergantianguru FROM pergantian_guru ORDER BY id_pergantianguru DESC LIMIT 1");
            
            if(rs.next()) {
                //ambil angka setelah ABS
                String id = rs.getString("id_pergantianguru").substring(3);
                int no = Integer.parseInt(id) + 1;
                
                tID.setText(String.format("PRG%03d", no));
            }else{
                tID.setText("PRG001");
            }
            rs.close();
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal generate ID : " + e.getMessage());
        }
    }

