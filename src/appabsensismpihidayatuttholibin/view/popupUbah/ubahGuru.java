    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        // TODO add your handling code here:
        
        String ID = tID.getText();
        String NIP = tNIP1.getText();
        String namaGuru = tNama.getText();
        String jenisKelamin = cJK.getSelectedItem().toString();
        String jk = null;

        // Mengonversi pilihan jenis kelamin ke kode (L/P)
        switch (jenisKelamin) {
            case "Laki-Laki":
                jk = "L";
                break;
            case "Perempuan":
                jk = "P";
                break;
            default:
                jk = null;
                break;
        }

        Date tglLahirDate = tTanggal.getDate();
        String tgl = new SimpleDateFormat("yyyy-MM-dd").format(tglLahirDate);
        String alamat = tAlamat.getText();
        String hp = tHP.getText();

        try {
            String sql = "UPDATE guru SET nip=?, nama_guru=?, jenis_kelamin=?, tgl_lahir=?, alamat=?, no_telepon=? WHERE id_guru=?";
            Connection conn = Koneksi.konek();
            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, NIP);
            ps.setString(2, namaGuru);
            ps.setString(3, jk);
            ps.setString(4, tgl);
            ps.setString(5, alamat);
            ps.setString(6, hp);
            ps.setString(7, ID);

            ps.executeUpdate();
            JOptionPane.showInternalMessageDialog(null, "Data berhasil diubah");
            pG.load_tabel_guru();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal diubah");
        }
        dispose();
    }//GEN-LAST:event_bUbahActionPerformed

