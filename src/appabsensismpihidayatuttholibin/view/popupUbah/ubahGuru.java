    public void setDataGuru(String idGuru, String NIP, String namaGuru, String JK,
            String tgl, String alamat, String hp) {

        tID.setText(idGuru);
        tNIP1.setText(NIP);

        tNama.setText(namaGuru);

        if (JK.equals("L")) {
            cJK.setSelectedItem("Laki-Laki");
        } else {
            cJK.setSelectedItem("Perempuan");
        }

        // Jika tanggal lahir tidak null dan tidak kosong, ubah ke format Date dan tampilkan di komponen kalender
        if (tgl != null && !tgl.isEmpty()) {
            try {
                tTanggal.setDate(java.sql.Date.valueOf(tgl));
            } catch (IllegalArgumentException e) {
                // Jika gagal parsing tanggal, kosongkan field tanggal
                tTanggal.setDate(null);
            }
        } else {
            tTanggal.setDate(null);
        }

        tAlamat.setText(alamat);
        tHP.setText(hp);
    }

